// lecture à 0
int read_nb = 0;

// === Écran OLED ===
Adafruit_SSD1306 display(128, 64, &Wire);

// === Conversion tension → mg/L ===
// FIX: Utilise la variable dynamique tension_air_propre au lieu de la constante
float tension_vers_mgl(float tension) {
  if (tension <= tension_air_propre) return 0.0f;
  float ratio = tension / tension_air_propre;
  float mgl = 0.025f * powf(ratio, 2.2f);
  return mgl;
}

// === Lecture capteur (moyenne de 5 échantillons) ===
float lire_tension() {
  float somme = 0.0f;
  for (int i = 0; i < 5; i++) {
    somme += (analogRead(MQ3_PIN) / 4095.0f) * 3.3f;
    delay(10);
  }
  return somme / 5.0f;
}

// === Setup ===
void setup() {
  Serial.begin(115200);
  pinMode(MQ3_PIN, INPUT);
  pinMode(BUTTON_PIN, INPUT_PULLUP);

  // === CONNEXION ===
  initialiser_connexion(ssid, password);

  if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDR)) {
    Serial.println(F("Erreur OLED"));
    for (;;);
  }

  // === Logo au démarrage ===
  display.clearDisplay();
  display.drawBitmap(32, 0, logo_ethyconnect, 64, 64, SSD1306_WHITE);
  display.display();
  delay(5000);

  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);
  display.setTextSize(2);
  display.setCursor(0, 18);
  display.println("Ethy");
  display.setCursor(0, 28);
  display.println("Connect");
  display.setCursor(0, 42);
  display.println("v1.1");
  display.display();
  delay(3000);

  // =========================================================
  // FIX: Chauffe capteur MQ3 — 30 secondes avec compte à rebours
  // Le MQ3 nécessite une montée en température pour être stable
  // =========================================================
  display.clearDisplay();
  display.setTextSize(2);
  display.setCursor(0, 0);
  display.println("Chauffe");
  display.setCursor(0, 14);
  display.println("Capteur");

  display.setTextSize(2);
  for (int i = 30; i > 0; i--) {
    display.fillRect(0, 30, 128, 34, SSD1306_BLACK);
    display.setCursor(30, 35);
    display.print(i);
    display.print("s");
    display.display();
    delay(1000);
  }

  // =========================================================
  // FIX: Calibration dynamique — lecture de la tension en air propre
  // Moyenne sur 20 lectures pour plus de stabilité
  // =========================================================
  display.clearDisplay();
  display.setTextSize(2);
  display.setCursor(0, 24);
  display.println("Calibrage");
  display.display();

  float somme_calib = 0.0f;
  for (int i = 0; i < 20; i++) {
    somme_calib += lire_tension();
    delay(100);
  }
  tension_air_propre = somme_calib / 20.0f;  // ✅ Valeur réelle stockée

  // Sécurité : on rejette les valeurs aberrantes
  if (tension_air_propre < 0.05f || tension_air_propre > 2.0f) {
    tension_air_propre = 0.25f;  // fallback
    Serial.println("WARN: Calibration hors plage, valeur par défaut utilisée.");
  }

  Serial.print("Calibration : tension_air_propre = ");
  Serial.print(tension_air_propre, 3);
  Serial.println(" V");

  // Affichage confirmation calibration
  display.clearDisplay();
  display.setTextSize(2);
  display.setCursor(0, 20);
  display.print("Base: ");
  display.print(tension_air_propre, 3);
  display.println(" V");
  display.display();
}

// === Boucle principale ===
void loop() {
  if (read_nb == 0) {

    // Compte à rebours de préparation
    display.clearDisplay();
    display.setTextSize(2);
    display.setCursor(0, 24);
    display.println("Pret !");
    display.display();
    delay(10000);

    display.clearDisplay();
    for (int i = 3; i > 0; i--) {
      display.fillRect(0, 20, 128, 20, SSD1306_BLACK);
      display.setTextSize(3);
      display.setCursor(10, 20);
      display.print(i);
      display.display();
      delay(1000);
    }

    // Mesure sur 4 secondes
    display.clearDisplay();
    display.setTextSize(2);
    display.setCursor(0, 24);
    display.println("Soufflez !");
    display.display();

    unsigned long t0 = millis();
    float somme = 0.0f;
    int compteur = 0;

    while (millis() - t0 < 4000) {
      somme += lire_tension();
      compteur++;
      delay(200);
    }

    float tension_moy = somme / compteur;
    float mgl = tension_vers_mgl(tension_moy);

    // Affichage résultat
    display.clearDisplay();
    display.setTextSize(2);
    display.setCursor(0, 0);
    display.println("Resultat");
    display.setCursor(0, 25);
    display.print(mgl, 2);
    display.println(" mg/L");
    display.display();

    // === ENVOIE DES DONNER ===
    send_data(mgl);
    serial.print("Données envoyé vers le server !");

    Serial.print("Tension moy : ");
    Serial.print(tension_moy, 3);
    Serial.print(" V  ->  ");
    Serial.print(mgl, 3);
    Serial.println(" mg/L");

    delay(5000);
    read_nb = 1;
  }
}
